namespace BoostBlasters.UI
{
    /// <summary>
    /// The different sounds used for menu transitions.
    /// </summary>
    public enum TransitionSound
    {
        /// <summary>
        /// No sound is used for this transition.
        /// </summary>
        None,
        /// <summary>
        /// The sound for when a menu is first opened.
        /// </summary>
        Open,
        /// <summary>
        /// The sound for advancing to a new menu screen.
        /// </summary>
        Next,
        /// <summary>
        /// The sound for going back out of a menu screen.
        /// </summary>
        Back,
    }
}
